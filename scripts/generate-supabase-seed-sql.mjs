#!/usr/bin/env node
/**
 * Genera INSERT SQL desde los JSON locales de Electron (userData).
 *
 * Uso:
 *   node scripts/generate-supabase-seed-sql.mjs
 *   node scripts/generate-supabase-seed-sql.mjs --user-data "/path/to/mytools-app"
 *
 * Salida: supabase/seed/generated/002_seed_from_local.sql
 */

import { readFileSync, mkdirSync, writeFileSync, existsSync } from 'node:fs'
import { join } from 'node:path'
import { homedir } from 'node:os'

const ROOT = join(import.meta.dirname, '..')
const OUT_DIR = join(ROOT, 'supabase', 'seed', 'generated')
const OUT_FILE = join(OUT_DIR, '002_seed_from_local.sql')

function defaultUserData() {
  const darwin = join(homedir(), 'Library', 'Application Support', 'mytools-app')
  const win = join(process.env.APPDATA || '', 'mytools-app')
  const linux = join(homedir(), '.config', 'mytools-app')
  if (process.platform === 'win32') return win
  if (process.platform === 'linux') return linux
  return darwin
}

function parseArgs() {
  const i = process.argv.indexOf('--user-data')
  const userData = i >= 0 ? process.argv[i + 1] : defaultUserData()
  return { userData }
}

function readJson(path) {
  if (!existsSync(path)) {
    console.warn(`skip (missing): ${path}`)
    return null
  }
  return JSON.parse(readFileSync(path, 'utf8'))
}

/** Escape string for PostgreSQL single-quoted literal */
function sqlStr(value) {
  if (value === null || value === undefined) return 'NULL'
  return `'${String(value).replace(/'/g, "''")}'`
}

function sqlBool(v) {
  if (v === null || v === undefined) return 'NULL'
  return v ? 'true' : 'false'
}

function sqlNum(v) {
  if (v === null || v === undefined || v === '') return 'NULL'
  const n = Number(v)
  return Number.isFinite(n) ? String(n) : 'NULL'
}

function sqlTextArray(tags) {
  if (!tags?.length) return "'{}'"
  const inner = tags.map((t) => sqlStr(t)).join(', ')
  return `ARRAY[${inner}]::text[]`
}

function main() {
  const { userData } = parseArgs()
  console.log('userData:', userData)

  const servicios = readJson(join(userData, 'gastos-servicios.json')) ?? []
  const pagos = readJson(join(userData, 'gastos-pagos.json')) ?? []
  const credenciales = readJson(join(userData, 'gastos-credenciales.json')) ?? []
  const queries = readJson(join(userData, 'queries.json')) ?? []

  const lines = []
  lines.push('-- Generated by scripts/generate-supabase-seed-sql.mjs')
  lines.push(`-- Source: ${userData}`)
  lines.push(`-- ${new Date().toISOString()}`)
  lines.push('-- Ejecutar DESPUÉS de 001_gastos_schema.sql')
  lines.push('')
  lines.push('BEGIN;')
  lines.push('')
  lines.push('-- Limpiar datos previos (opcional; comentá si no querés borrar)')
  lines.push('TRUNCATE public.pagos, public.credenciales, public.queries, public.servicios CASCADE;')
  lines.push('')

  if (servicios.length) {
    lines.push('-- servicios')
    for (const s of servicios) {
      lines.push(
        `INSERT INTO public.servicios (id, nombre, emoji, numero_cuenta, categoria, activo, orden) VALUES (${sqlStr(s.id)}, ${sqlStr(s.nombre)}, ${sqlStr(s.emoji)}, ${s.numeroCuenta != null && s.numeroCuenta !== '' ? sqlStr(s.numeroCuenta) : 'NULL'}, ${sqlStr(s.categoria)}, ${sqlBool(s.activo)}, ${sqlNum(s.orden)}) ON CONFLICT (id) DO UPDATE SET nombre = EXCLUDED.nombre, emoji = EXCLUDED.emoji, numero_cuenta = EXCLUDED.numero_cuenta, categoria = EXCLUDED.categoria, activo = EXCLUDED.activo, orden = EXCLUDED.orden, updated_at = now();`,
      )
    }
    lines.push('')
  }

  if (pagos.length) {
    lines.push('-- pagos')
    for (const p of pagos) {
      lines.push(
        `INSERT INTO public.pagos (id, servicio_id, mes, monto, fecha, metodo_pago, pagado, notas) VALUES (${sqlStr(p.id)}, ${sqlStr(p.servicioId)}, ${sqlStr(p.mes)}, ${sqlNum(p.monto)}, ${p.fecha ? sqlStr(p.fecha) : 'NULL'}, ${p.metodoPago ? sqlStr(p.metodoPago) : 'NULL'}, ${sqlBool(p.pagado)}, ${p.notas ? sqlStr(p.notas) : 'NULL'}) ON CONFLICT (id) DO UPDATE SET servicio_id = EXCLUDED.servicio_id, mes = EXCLUDED.mes, monto = EXCLUDED.monto, fecha = EXCLUDED.fecha, metodo_pago = EXCLUDED.metodo_pago, pagado = EXCLUDED.pagado, notas = EXCLUDED.notas, updated_at = now();`,
      )
    }
    lines.push('')
  }

  if (credenciales.length) {
    lines.push('-- credenciales (password_enc vacío: las contraseñas locales están vacías o deben cargarse vía app)')
    for (const c of credenciales) {
      lines.push(
        `INSERT INTO public.credenciales (id, nombre, usuario, password_enc, url, notas, categoria, orden) VALUES (${sqlStr(c.id)}, ${sqlStr(c.nombre)}, ${sqlStr(c.usuario)}, ${sqlStr('')}, ${c.url ? sqlStr(c.url) : 'NULL'}, ${c.notas ? sqlStr(c.notas) : 'NULL'}, ${c.categoria ? sqlStr(c.categoria) : 'NULL'}, ${sqlNum(c.orden)}) ON CONFLICT (id) DO UPDATE SET nombre = EXCLUDED.nombre, usuario = EXCLUDED.usuario, url = EXCLUDED.url, notas = EXCLUDED.notas, categoria = EXCLUDED.categoria, orden = EXCLUDED.orden, updated_at = now();`,
      )
    }
    lines.push('')
  }

  if (queries.length) {
    lines.push('-- queries')
    for (const q of queries) {
      lines.push(
        `INSERT INTO public.queries (id, motor, descripcion, query, tags, orden) VALUES (${sqlStr(q.id)}, ${sqlStr(q.motor)}, ${sqlStr(q.descripcion)}, ${sqlStr(q.query)}, ${sqlTextArray(q.tags)}, ${sqlNum(q.orden)}) ON CONFLICT (id) DO UPDATE SET motor = EXCLUDED.motor, descripcion = EXCLUDED.descripcion, query = EXCLUDED.query, tags = EXCLUDED.tags, orden = EXCLUDED.orden, updated_at = now();`,
      )
    }
    lines.push('')
  }

  lines.push('COMMIT;')
  lines.push('')
  lines.push(`-- Resumen: servicios=${servicios.length}, pagos=${pagos.length}, credenciales=${credenciales.length}, queries=${queries.length}`)

  mkdirSync(OUT_DIR, { recursive: true })
  writeFileSync(OUT_FILE, lines.join('\n'), 'utf8')
  console.log('Wrote:', OUT_FILE)
  console.log(`Counts: servicios=${servicios.length}, pagos=${pagos.length}, credenciales=${credenciales.length}, queries=${queries.length}`)
}

main()
